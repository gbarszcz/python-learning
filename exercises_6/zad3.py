# Utwórz za pomocą virtualenv własne środowisko. Doinstaluj do niego pakiet pep8 i sprawdź zgodność własnych skryptów ze standardem pep8.
# > pip install virtualenv
# > virtualenv env pep8 // virtualenv [nazwa srodowiska]
# > env pep8\Scripts\activate //aktywacja srodowiska
# > pip install pep8
# > pep8 --first test.py // weryfikacja srodowiska i pakietu. w 'test.py' tylko 'print 'costam'
# > deactivate // wylaczenie
